// non conditional
function isOdd(num) {
  return num % 2 !== 0;
}
console.log(isOdd(2));
// conditional
function isOdd(number) {
  if (number % 2 === 0) {
    return false;
  } else {
    return true;
  }
}
console.log(isOdd(1));

function plusFive(num) {
  return (num += 5);
}
console.log(plusFive(1));
